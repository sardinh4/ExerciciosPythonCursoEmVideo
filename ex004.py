d1 = input('Digite algo: ')
print('Especificações de {}.'.format(d1))
print('Tipo:', type(d1))
print('É Alfabético:', d1.isalpha())
print('É Númerico?', d1.isnumeric())
print('Possui algum dos dois acima?', d1.isalnum())
print('É um espaço?', d1.isspace())
print('Possui apenas letras minúsculas?', d1.islower())
print('Possui apenas letras maiúsculas?', d1.isupper())
print('Possui os dois tipos acima?', d1.istitle())




